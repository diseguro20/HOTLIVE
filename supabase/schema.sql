create or replace function public.process_vizzion_payment_event(
  p_event_type text,
  p_provider_transaction_id text,
  p_external_reference text,
  p_offer_code text,
  p_checkout_url text,
  p_customer_email text,
  p_status text,
  p_card_brand text,
  p_card_last4 text,
  p_payload jsonb,
  p_idempotency_key text
)
returns table(order_id uuid, order_status text, coins_credited boolean)
language plpgsql
security definer
set search_path = public
as $$
declare
  target_order public.payment_orders%rowtype;
  inserted_event_id bigint;
  should_credit boolean := false;
begin
  insert into public.payment_events (
    provider_transaction_id,
    event_type,
    idempotency_key,
    payload
  ) values (
    nullif(p_provider_transaction_id, ''),
    p_event_type,
    p_idempotency_key,
    coalesce(p_payload, '{}'::jsonb)
  )
  on conflict (idempotency_key) do nothing
  returning id into inserted_event_id;

  if inserted_event_id is null then
    select po.id, po.status, po.credited_at is not null
      into order_id, order_status, coins_credited
    from public.payment_orders po
    where po.provider_transaction_id = nullif(p_provider_transaction_id, '')
       or po.external_reference = nullif(p_external_reference, '')
    order by po.created_at desc
    limit 1;
    return next;
    return;
  end if;

  select po.* into target_order
  from public.payment_orders po
  where (nullif(p_provider_transaction_id, '') is not null
         and po.provider_transaction_id = p_provider_transaction_id)
     or (nullif(p_external_reference, '') is not null
         and po.external_reference = p_external_reference)
     or (nullif(p_offer_code, '') is not null
         and po.offer_code = p_offer_code
         and lower(po.customer_email) = lower(coalesce(p_customer_email, ''))
         and po.credited_at is null)
  order by
    case when po.provider_transaction_id = p_provider_transaction_id then 0
         when po.external_reference = p_external_reference then 1
         else 2 end,
    po.created_at desc
  limit 1
  for update;

  if target_order.id is null then
    update public.payment_events
    set processed_at = now(), processing_error = 'Pedido correspondente nao encontrado'
    where id = inserted_event_id;
    return;
  end if;

  should_credit := p_event_type = 'TRANSACTION_PAID' and target_order.credited_at is null;

  update public.payment_orders
  set provider_transaction_id = coalesce(nullif(p_provider_transaction_id, ''), provider_transaction_id),
      offer_code = coalesce(nullif(p_offer_code, ''), offer_code),
      checkout_url = coalesce(nullif(p_checkout_url, ''), checkout_url),
      status = coalesce(nullif(p_status, ''), status),
      card_brand = coalesce(nullif(p_card_brand, ''), card_brand),
      card_last4 = coalesce(nullif(p_card_last4, ''), card_last4),
      provider_payload = coalesce(p_payload, provider_payload),
      paid_at = case when p_event_type = 'TRANSACTION_PAID' then coalesce(paid_at, now()) else paid_at end,
      credited_at = case when should_credit then now() else credited_at end
  where id = target_order.id;

  if should_credit then
    update public.profiles
    set coins = coins + target_order.coins + target_order.bonus
    where id = target_order.user_id;
  end if;

  update public.payment_events
  set order_id = target_order.id, processed_at = now()
  where id = inserted_event_id;

  order_id := target_order.id;
  order_status := coalesce(nullif(p_status, ''), target_order.status);
  coins_credited := should_credit;
  return next;
end;
$$;

revoke all on function public.process_vizzion_payment_event(
  text, text, text, text, text, text, text, text, text, jsonb, text
) from public, anon, authenticated;
grant execute on function public.process_vizzion_payment_event(
  text, text, text, text, text, text, text, text, text, jsonb, text
) to service_role;

