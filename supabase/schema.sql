create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text not null default '',
  avatar_url text,
  coins bigint not null default 0 check (coins >= 0),
  superlive_user_id text unique,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.profiles enable row level security;

drop policy if exists "Profiles are readable by authenticated users" on public.profiles;
create policy "Profiles are readable by authenticated users"
on public.profiles for select
to authenticated
using (true);

drop policy if exists "Users update their own profile" on public.profiles;
create policy "Users update their own profile"
on public.profiles for update
to authenticated
using (auth.uid() = id)
with check (auth.uid() = id);

-- Financial balance is read-only to browser clients. Credit it only from a
-- trusted payment webhook using the Supabase service role.
revoke update on public.profiles from authenticated;
grant update (full_name, avatar_url, superlive_user_id) on public.profiles to authenticated;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.profiles (id, full_name)
  values (new.id, coalesce(new.raw_user_meta_data ->> 'full_name', ''));
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists profiles_set_updated_at on public.profiles;
create trigger profiles_set_updated_at
  before update on public.profiles
  for each row execute procedure public.set_updated_at();

create table if not exists public.payment_orders (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete restrict,
  provider text not null default 'vizzion_pay',
  provider_transaction_id text unique,
  external_reference text not null unique,
  package_id text not null,
  coins bigint not null check (coins > 0),
  bonus bigint not null default 0 check (bonus >= 0),
  amount numeric(12, 2) not null check (amount > 0),
  currency text not null default 'BRL',
  method text not null check (method in ('pix', 'credit_card')),
  status text not null default 'created',
  offer_code text,
  checkout_url text,
  customer_name text,
  customer_email text,
  customer_phone_last4 text,
  customer_document_last4 text,
  billing_address jsonb not null default '{}'::jsonb,
  card_brand text,
  card_last4 text,
  provider_fee numeric(12, 2) not null default 0 check (provider_fee >= 0),
  operational_cost numeric(12, 2) not null default 0 check (operational_cost >= 0),
  refunded_amount numeric(12, 2) not null default 0 check (refunded_amount >= 0),
  webhook_token_hash text,
  provider_payload jsonb not null default '{}'::jsonb,
  paid_at timestamptz,
  credited_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

